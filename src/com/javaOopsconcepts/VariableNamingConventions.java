package com.javaOopsconcepts;

public class VariableNamingConventions {

	public static void main(String[] args) {
		//variable name should not be same as java keyword
		
		//int int =100;
		//variable name should not be duplicate
		int a=100;
		//int a=200;not be duplicate 
		a=200;
		System.out.println(a);
		//variable name should start with alphanumeric
		int firstNumber=100;
		//int 1number=100;
		//variable name should not have any special character except "_" and "$"
	  // boolean first@number=true;
	   boolean first_Number=true;
	    
		

	}

}
